/**
* rev_string - string reversal f
*@s: input string
*
* Return: No return val
*/

#include "main.h"

void rev_string(char *s)
{
	int i;
	int count;
	int list[];
	int j;

	for (i = 0; s[i] != '\0'; i++)
	{
	count += 1;
	}

	for (i = --count && j = 0; i >= 0 && j <= --count; i--, j++)
	{
	int list[j] = s[i];
	}

	s = list;

}

/**
* op_add - operation to add
*@a: input
*@b: input
*
* Return: sum of a and b
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - operation to sub
*@a: input
*@b: input
*
* Return: sub of b from a
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - operation to mul
*@a: input
*@b: input
*
* Return: mul of a and b
*/

int op_mul(int a, int b)
{
	return (a * b);
}


/**
* op_div - operation to div
*@a: input
*@b: input
*
* Return: div of a by b
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - operation to mod
*@a: input
*@b: input
*
* Return: mod of a by b
*/

int op_mod(int a, int b)
{
	return (a % b);
}

